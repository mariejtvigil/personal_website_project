					<h2>Cognitive Marketing<h2>
							<p class="paragraph">
								With Cognitive Design, Design Thinking, User Experience Design, and the use of data warehouse architecture many possible answers are evaluated
								to define one final solution. Asking key questions focused on the root of
								a situation is how simple, resourceful, and intuitive solutions are built. In other words, it’s more important to solve the right problem than to design the perfect solution.
							</p>
							<p class="paragraph">
								As designers within a business,  it’s our job to solve a business problem first, than create a thing of beauty second.
								We need to frame design problems as business problems as well as dissect user concerns though a scientific microscope.
								Collaboration with subject matter experts, stakeholders, and research of user's needs will help identify what features and functions will result in a
								great user experience AND return on investment for both the user and the business.
							</p>
							<p class="paragraph">
								Product development and design is a cyclical process. Creativity during the innovation phase is often based on user experience and artistry.
								Collected data during the investigation phase can direct designers and developers to a broken step in the user flow.
								Data that directs our attention to the problem won’t tell us how to fix the problem but aids us in finding a solution.
							</p>
							<p class="paragraph">
								Data, analysis, and strategic planning should ultimately be part of the design process.
								Designers can learn from other departments wealth of data.
								Applying the right data at the right time within the right design stage is an important element to the future of design.
								Marketing, design, and development go hand-in-hand in building, launching, and selling a successful product.
								Seeking understanding of the complexities of everyday human experience and merging them with business goals leads to a cognitive marketing approach to design.
							</p>
							<p class="paragraph">
								Remember to keep it simple, make it useful, practice empathy, apply constraints, have fun, and don't take yourself too serious.
							</p>
