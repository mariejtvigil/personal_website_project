				<h2>Staying Focused</h2>
				<p class="focus-paragraph">
					Often we lose focus within the design and development stages. We get distracted, off-track, or scope creep sets in.
					Scope creep occurs when the scope of a project is not properly defined, documented, or controlled.
					Scope creep is harmful to the project and often causes damage to a team environment.
					Collaboration, mutual respect, open communication, and teamwork are the keys to successful problem
					solving as much as asking the right questions and doing the right amount of research at the beginning of the project.
					Don't let scope creep complicate the design and development process. Know when to say no and when to make the right exception for the right reason.
				</p>
